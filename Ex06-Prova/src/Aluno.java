
public class Aluno {

	private String nome;
	private int anoIngresso;
	
	public Aluno(String nome, int anoIngresso) {
		this.nome = nome;
		this.anoIngresso = anoIngresso;
	}
	
	public String getNome() {
		return this.nome;
	}
	public int getAnoIngresso() {
		return anoIngresso;
	}
	
	
}
